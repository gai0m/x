// Function to hide story overlay
function hideStory() {
    overlay.classList.remove('active');
    document.body.style.overflow = 'auto'; // Restore scrolling
    
    // Reset any special effects
    setTimeout(() => {
        resetAnimations();
    }, 400);
}

// Enhanced hover effects for interactive areas
function addHoverEffects() {
    interactiveAreas.forEach(area => {
        area.addEventListener('mouseenter', function() {
            this.style.transform = 'scale(1.05) rotate(1deg)';
            this.style.boxShadow = '0 0 20px rgba(212, 161, 91, 0.8)';
        });
        
        area.addEventListener('mouseleave', function() {
            this.style.transform = 'scale(1) rotate(0deg)';
            this.style.boxShadow = '0 0 15px rgba(212, 161, 91, 0.6)';
        });
    });
}

// Event listeners
document.addEventListener('DOMContentLoaded', function() {
    // Initialize audio
    initializeAudio();
    
    // Language toggle functionality
    languageBtn.addEventListener('click', switchLanguage);
    
    // Add click listeners to interactive areas
    interactiveAreas.forEach(area => {
        area.addEventListener('click', function() {
            const storyKey = this.getAttribute('data-story');
            showStory(storyKey);
        });
    });
    
    // Add enhanced hover effects
    addHoverEffects();
    
    // Close button listener
    closeButton.addEventListener('click', hideStory);
    
    // Close overlay when clicking outside content
    overlay.addEventListener('click', function(e) {
        if (e.target === overlay) {
            hideStory();
        }
    });
    
    // Close overlay with Escape key
    document.addEventListener('keydown', function(e) {
        if (e.key === 'Escape' && overlay.classList.contains('active')) {
            hideStory();
        }
    });
});

