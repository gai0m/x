const backgroundAudio = document.getElementById('background-audio');
const audioToggle = document.getElementById('audio-toggle');
const playIcon = document.querySelector('.play-icon');
const pauseIcon = document.querySelector('.pause-icon');
const volumeSlider = document.getElementById('volume-slider');

// Audio state
let isPlaying = false;

// Language switching functionality
function switchLanguage() {
    currentLanguage = currentLanguage === 'en' ? 'ar' : 'en';
    
    // Toggle RTL class on body
    if (currentLanguage === 'ar') {
        document.body.classList.add('rtl');
    } else {
        document.body.classList.remove('rtl');
    }
    
    // Update all elements with data-en and data-ar attributes
    const elementsToTranslate = document.querySelectorAll('[data-en][data-ar]');
    elementsToTranslate.forEach(element => {
        element.textContent = element.getAttribute(`data-${currentLanguage}`);
    });
}

// Audio functionality
function initializeAudio() {
    // Set initial volume
    backgroundAudio.volume = volumeSlider.value / 100;
    
    // Audio toggle functionality
    audioToggle.addEventListener('click', function() {
        if (isPlaying) {
            backgroundAudio.pause();
            playIcon.style.display = 'flex';
            pauseIcon.style.display = 'none';
            isPlaying = false;
        } else {
            backgroundAudio.play().then(() => {
                playIcon.style.display = 'none';
                pauseIcon.style.display = 'flex';
                isPlaying = true;
            }).catch(error => {
                console.log('Audio play failed:', error);
            });
        }
    });
    
    // Volume control
    volumeSlider.addEventListener('input', function() {
        backgroundAudio.volume = this.value / 100;
    });
    
    // Handle audio end (though it's set to loop)
    backgroundAudio.addEventListener('ended', function() {
        playIcon.style.display = 'flex';
        pauseIcon.style.display = 'none';
        isPlaying = false;
    });
    
    // Handle audio errors
    backgroundAudio.addEventListener('error', function(e) {
        console.log('Audio error:', e);
        playIcon.style.display = 'flex';
        pauseIcon.style.display = 'none';
        isPlaying = false;
    });
}

// Function to reset animations
function resetAnimations() {
    const animatedElements = storyText.querySelectorAll('h2, h3, p');
    animatedElements.forEach(element => {
        element.style.animation = 'none';
        element.offsetHeight; // Trigger reflow
        element.style.animation = null;
    });
}

// Function to show story overlay with enhanced animations
function showStory(storyKey) {
    const story = storyContent[storyKey];
    if (!story) return;
    
    // Clear previous content
    storyText.innerHTML = '';
    
    // Create title with typing effect for special stories
    const title = document.createElement('h2');
    title.textContent = story.title[currentLanguage];
    if (storyKey === 'iraq-text' || storyKey === 'cuneiform' || storyKey === 'ancient-script') {
        title.classList.add('typing-effect');
    }
    storyText.appendChild(title);
    
    // Create sections with staggered animations
    story.sections.forEach((section, index) => {
        const heading = document.createElement('h3');
        heading.textContent = section.heading[currentLanguage];
        heading.style.animationDelay = `${0.3 + (index * 0.3)}s`;
        storyText.appendChild(heading);
        
        const paragraph = document.createElement('p');
        paragraph.textContent = section.text[currentLanguage];
        paragraph.style.animationDelay = `${0.4 + (index * 0.3)}s`;
        storyText.appendChild(paragraph);
    });
    
    // Show overlay with animation
    overlay.classList.add('active');
    document.body.style.overflow = 'hidden'; // Prevent background scrolling
    
    // Add special effects for certain stories
    setTimeout(() => {
        if (storyKey === 'ishtar-gate') {
            addShimmerEffect();
        } else if (storyKey === 'cuneiform' || storyKey === 'ancient-script') {
            addAncientTextEffect();
        } else if (storyKey === 'ancient-rulers') {
            addRoyalEffect();
        }
    }, 1000);
}

